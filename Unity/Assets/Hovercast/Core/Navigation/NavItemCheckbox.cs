namespace Hovercast.Core.Navigation {

	/*================================================================================================*/
	public class NavItemCheckbox : NavItem<bool> {

		
		////////////////////////////////////////////////////////////////////////////////////////////////
		/*--------------------------------------------------------------------------------------------*/
		public override NavItem.ItemType Type {
			get {
				return NavItem.ItemType.Checkbox;
			}
		}


		////////////////////////////////////////////////////////////////////////////////////////////////
		/*--------------------------------------------------------------------------------------------*/
		public override void Select() {
			Value = !Value;
			base.Select();
		}

	}

}
