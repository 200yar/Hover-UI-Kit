using UnityEngine;

namespace Hover.Engines.Unity {

	/*================================================================================================*/
	public class Engine : IEngine {

		public IEngineMath Math { get; private set; }


		////////////////////////////////////////////////////////////////////////////////////////////////
		/*--------------------------------------------------------------------------------------------*/
		public Engine() {
			Math = new EngineMath();
		}


		////////////////////////////////////////////////////////////////////////////////////////////////
		/*--------------------------------------------------------------------------------------------*/
		public IContainer FindContainer(string pName) {
			return new ContainerWrap(GameObject.Find(pName));
		}

	}

}
