namespace Hover.Engines {

	/*================================================================================================*/
	public struct Quaternion {

		public float W;
		public float X;
		public float Y;
		public float Z;


		////////////////////////////////////////////////////////////////////////////////////////////////
		/*--------------------------------------------------------------------------------------------*/
		public Quaternion(float pW=0, float pX=0, float pY=0, float pZ=0) {
			W = pW;
			X = pX;
			Y = pY;
			Z = pZ;
		}

	}

}
