namespace Hover.Engines {

	/*================================================================================================*/
	public struct Vector2 {

		public float X;
		public float Y;


		////////////////////////////////////////////////////////////////////////////////////////////////
		/*--------------------------------------------------------------------------------------------*/
		public Vector2(float pX, float pY) {
			X = pX;
			Y = pY;
		}


		////////////////////////////////////////////////////////////////////////////////////////////////
		/*--------------------------------------------------------------------------------------------*/
		public static Vector2 operator +(Vector2 pA, Vector2 pB) {
			return new Vector2(pA.X+pB.X, pA.Y+pB.Y);
		}

		/*--------------------------------------------------------------------------------------------*/
		public static bool operator ==(Vector2 pA, Vector2 pB) {
			return (pA.X == pB.X && pA.Y == pB.Y);
		}
		
		/*--------------------------------------------------------------------------------------------*/
		public static bool operator !=(Vector2 pA, Vector2 pB) {
			return !(pA == pB);
		}

	}

}
